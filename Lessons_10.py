class Rectangle:
    def __init__(self, side_1, side_2=None):
        self.side_1 = side_1
        self.side_2 = side_2 if side_2 is not None else side_1

    def area(self):
        return self.side_2 * self.side_1

    def perimetr(self):
        return 2 * (self.side_1 + self.side_2)


